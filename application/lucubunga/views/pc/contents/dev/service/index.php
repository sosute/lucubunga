    <h2>ESTA（電子渡航認証システム）について</h2>
    <p>通常、米国への渡航時にはビザ取得が必要となりますが、VWP参加国の国籍を持つ渡航者が渡米する際、短期商用・観光目的で滞在日数が90日以下など幾つかの要件を満たした場合に、ビザ取得手続きを免除し、比較的簡便な電子認証にて渡航可能となる制度です。米国にて乗り継ぎをする場合にもESTA認証が必要となります。就労や留学などで渡航目的が上記に該当しない場合は、目的に応じてビザを取得して渡米する必要があります。また犯罪歴の有無や伝染病の有無等により、VWP参加国のすべての方がビザ免除プログラムを利用できるというわけではありません。 </p>
    <p class="mt10">ESTA認証の有効期限は、原則として認証日から2年間となります。但し、2年以内にパスポートの有効期限が満了する場合は、ESTA認証もパスポート有効期限をもって失効となります。また、パスポートの再発行を行った場合は、新しいパスポート番号にて改めて認証を受ける必要があります。</p>
    <p class="mt10">米国国土安全保障省（DHS）にオンライン申請書を提出してから、最長72時間の審査期間を経て認証結果が通知されます。その為、搭乗する72時間前までに申請を行う事が推奨されています。</p>
    <p class="mt10">米国国土安全保障省（DHS）の規定により、ESTA認証が拒否される場合がございます。認証拒否となった場合には、米国大使館にてビザを取得しなければ米国への渡航はできません。ビザ発行には時間がかかりますのでご注意下さい。その他、詳しい情報については、米国国土安全保障省サイトや、米国大使館サイトをご参照下さい。</p>
    <p class="mt10 surrounds">米国大使館サイト&nbsp;:&nbsp;<a href="http://japanese.japan.usembassy.gov/" target="_blank" class="a_color">http://japanese.japan.usembassy.gov/</a></p>
