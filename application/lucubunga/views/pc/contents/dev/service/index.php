    <h2>VWP（ビザ免除プログラム）について</h2>
    <h3 class="subtitle-arrow">VWP利用要件</h3>
    <p>下記要件を満たしアメリカ合衆国50州（ハワイ・アラスカを含む）もしくはアメリカから乗り継ぎで他国へ入国される方</p>
    <ul class="service">
        <li>後述の利用パスポート制約に抵触しない有効なパスポートをお持ちである事</li>
        <li>ESTAの認証許可を取得している事</li>
        <li>観光や知人訪問・ビジネスなど90日以内の短期滞在で、報酬を得る活動を行わない事</li>
        <li>往復（乗り継ぎの場合は次の目的地の）航空チケット、船舶チケットを持っている事</li>
        <li>VWP参加国の国籍を有している事</li>
      </ul>
    <h3 class="subtitle-arrow">利用パスポート制約</h3>
    <p>ビザ免除資格を持つ渡航者は、有効な機械読取式パスポート（現在、日本で発行されているパスポートは全て機械読取式となります）を所持する必要があります。尚、パスポート発行日により、下記の条件を満たす必要があります。</p>
    <ul class="service">
        <li>2006年10月26日以降に発行されている機械読取式パスポートは、Eパスポート（IC旅券）でなければなりません。</li>
        <li>2005年10月26日～2006年10月25日までに発行されている機械読取式パスポートは、写真付きIDページにデジタル写真が貼ってあること。</li>
        <li>2005年10月26日より以前に発行されているパスポートは機械読取式パスポートであること。</li>
        <li>ギリシャ、マルタ、チェコ、エストニア、ハンガリー、ラトビア、リトアニア、韓国、スロバキアの国籍の方は、パスポートの発行日に関わらず、Eパスポート（IC旅券）を所持していること。</li>
      </ul>
    <h3 class="subtitle-arrow">事前確認事項</h3>
    <p>過去にビザの取り消しをされた経歴がある方、有罪・無罪にかかわらず逮捕歴のある方、犯罪歴がある方、特定の伝染病を患っている方、過去に米国への入国を拒否されたり強制送還された方、過去にビザ免除プログラムで入国しオーバーステイした事がある方等は、ESTA認証を否認される事がございます。この場合、渡米に臨みビザを取得する必要があります。</p>
    <p class="mt10">軽微な犯罪で逮捕および訴追や有罪に至らないような場合には、ビザ免除プログラムを利用することができます。米国で交通違反で検挙され、罰金の支払いが未達となっている、または法廷審問に呼ばれたにもかかわらず出頭を拒否したような場合は、逮捕状が提出されている可能性があり、入国審査をパス出来ない事があります。該当事項のある方は、渡米前に予め問題を解消し、ESTA申請にて渡航許可を得るかビザの申請・取得を行う必要があります。</p>
